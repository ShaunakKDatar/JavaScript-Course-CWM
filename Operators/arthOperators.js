
let x=10;
let y=3;

console.log(x + y); // Addition
console.log(x - y); // Substration
console.log(x * y); // Multiplication
console.log(x / y); // Division
console.log(x % y); // Remainder
console.log(x ** y); // Exponential

// Increment (++)
console.log(++x); // will display 11 and make the value of x=11
console.log(x++); // will display 11 and make the value of x=12
console.log(x);

// Decrement(--)
console.log(--y); // will display 2 and make y=2
console.log(y--); // will display 2 and make y=1
console.log(y);